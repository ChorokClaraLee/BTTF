package kr.co.bttf.DTO;

public class Bookmark_DTO {

	
	private String user_id;
	private String post_id;
	private String language_index;
	private String post_subject;
	private String post_rec;
	private String writer;
	private String post_regdate;
	private String bookmark_id;
	
	public String getUser_id() {
		return user_id;
	}
	public void setUser_id(String user_id) {
		this.user_id = user_id;
	}
	public String getPost_id() {
		return post_id;
	}
	public void setPost_id(String post_id) {
		this.post_id = post_id;
	}
	public String getLanguage_index() {
		return language_index;
	}
	public void setLanguage_index(String language_index) {
		this.language_index = language_index;
	}
	public String getPost_subject() {
		return post_subject;
	}
	public void setPost_subject(String post_subject) {
		this.post_subject = post_subject;
	}
	public String getPost_rec() {
		return post_rec;
	}
	public void setPost_rec(String post_rec) {
		this.post_rec = post_rec;
	}
	public String getWriter() {
		return writer;
	}
	public void setWriter(String writer) {
		this.writer = writer;
	}
	public String getPost_regdate() {
		return post_regdate;
	}
	public void setPost_regdate(String post_regdate) {
		this.post_regdate = post_regdate;
	}
	public String getBookmark_id() {
		return bookmark_id;
	}
	public void setBookmark_id(String bookmark_id) {
		this.bookmark_id = bookmark_id;
	}
	
}
